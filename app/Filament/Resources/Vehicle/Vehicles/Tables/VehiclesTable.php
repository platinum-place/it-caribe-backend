<?php

namespace App\Filament\Resources\Vehicle\Vehicles\Tables;

use Filament\Actions\BulkActionGroup;
use Filament\Actions\DeleteBulkAction;
use Filament\Actions\EditAction;
use Filament\Actions\ForceDeleteBulkAction;
use Filament\Actions\RestoreBulkAction;
use Filament\Actions\ViewAction;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Filters\TrashedFilter;
use Filament\Tables\Table;

class VehiclesTable
{
    public static function configure(Table $table): Table
    {
        return $table
            ->columns([
                TextColumn::make('created_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
                TextColumn::make('updated_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
                TextColumn::make('deleted_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
                TextColumn::make('created_by')
                    ->numeric()
                    ->sortable(),
                TextColumn::make('updated_by')
                    ->numeric()
                    ->sortable(),
                TextColumn::make('deleted_by')
                    ->numeric()
                    ->sortable(),
                TextColumn::make('vehicleMake.name')
                    ->searchable(),
                TextColumn::make('vehicleModel.name')
                    ->searchable(),
                TextColumn::make('vehicleType.name')
                    ->searchable(),
                TextColumn::make('vehicleUse.name')
                    ->searchable(),
                TextColumn::make('vehicleActivity.name')
                    ->searchable(),
                TextColumn::make('vehicleLoanType.name')
                    ->searchable(),
                TextColumn::make('vehicleUtility.name')
                    ->searchable(),
                TextColumn::make('vehicle_year')
                    ->numeric()
                    ->sortable(),
                TextColumn::make('chassis')
                    ->searchable(),
                TextColumn::make('license_plate')
                    ->searchable(),
            ])
            ->filters([
                TrashedFilter::make(),
            ])
            ->recordActions([
                ViewAction::make(),
                EditAction::make(),
            ])
            ->toolbarActions([
                BulkActionGroup::make([
                    DeleteBulkAction::make(),
                    ForceDeleteBulkAction::make(),
                    RestoreBulkAction::make(),
                ]),
            ]);
    }
}
